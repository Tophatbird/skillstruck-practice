#How many numbers?

number = int(input()) #asks the user for an input
counter = 0 #the counter will go up for every number inputted thats not 0
while number > 0 : # this will make it go until 0 is input
    number = int(input()) #this is to repeat the input prompt
    counter += 1 #adds to the counter
print(counter) #prints the awnser

